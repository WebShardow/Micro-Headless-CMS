import { PrismaClient } from '@prisma/client';

const prisma = new PrismaClient();

async function main() {
  const privacyContent = `
    <h1>นโยบายความเป็นส่วนตัว (Privacy Policy)</h1>
    <p>อัปเดตล่าสุด: ${new Date().toLocaleDateString('th-TH')}</p>
    
    <h2>1. บทนำ</h2>
    <p>เราให้ความสำคัญอย่างยิ่งต่อการคุ้มครองข้อมูลส่วนบุคคลของคุณ นโยบายนี้อธิบายถึงวิธีที่เราเก็บรวบรวม ใช้ และเปิดเผยข้อมูลของคุณเมื่อคุณใช้งานแพลตฟอร์มของเรา ตามพระราชบัญญัติคุ้มครองข้อมูลส่วนบุคคล พ.ศ. 2562 (PDPA)</p>
    
    <h2>2. ข้อมูลที่เราเก็บรวบรวม</h2>
    <p>เราอาจเก็บรวบรวมข้อมูลดังต่อไปนี้: ชื่อ, ที่อยู่อีเมล, ข้อมูลการใช้งานเว็บไซตฺ และข้อมูลทางเทคนิคอื่นๆ ที่จำเป็นต่อการให้บริการ</p>
    
    <h2>3. วัตถุประสงค์ในการใช้ข้อมูล</h2>
    <ul>
      <li>เพื่อให้บริการและปรับปรุงคุณภาพของแพลตฟอร์ม</li>
      <li>เพื่อการสื่อสารและตอบข้อซักถามของผู้ใช้งาน</li>
      <li>เพื่อรักษาความละเอียดปลอดภัยของระบบ</li>
    </ul>
    
    <h2>4. สิทธิ์ของคุณ</h2>
    <p>ภายใต้กฎหมาย PDPA คุณมีสิทธิ์เข้าถึง แก้ไข ลบ หรือคัดค้านการประมวลผลข้อมูลส่วนบุคคลของคุณได้ทุกเมื่อ</p>
    
    <h2>5. การติดต่อเรา</h2>
    <p>หากคุณมีคำถามเกี่ยวกับนโยบายนี้ โปรดติดต่อผู้ดูแลระบบผ่านช่องทางที่ระบุในหน้าติดต่อเรา</p>
  `;

  const termsContent = `
    <h1>ข้อตกลงและเงื่อนไขการใช้งาน (Terms of Service)</h1>
    <p>ยินดีต้อนรับสู่แพลตฟอร์ม Micro-Team การเข้าใช้งานเว็บไซตฺถือว่าคุณยอมรับข้อตกลงดังต่อไปนี้:</p>
    
    <h2>1. ทรัพย์สินทางปัญญาและลิขสิทธิ์</h2>
    <p>เนื้อหาทั้งหมดบนแพลตฟอร์มนี้ รวมถึงแต่ไม่จำกัดเพียง ข้อความ รูปภาพ ซอร์สโค้ด และการออกแบบ เป็นทรัพย์สินทางปัญญาของเราหรือผู้ได้รับอนุญาต ห้ามมิให้ทำซ้ำ ดัดแปลง หรือนำไปใช้โดยไม่ได้รับอนุญาตเป็นลายลักษณ์อักษร</p>
    
    <h2>2. เงื่อนไขการใช้งาน</h2>
    <p>ผู้ใช้งานตกลงที่จะไม่ใช้งานแพลตฟอร์มในทางที่ผิดกฎหมาย หรือส่งผลกระทบต่อเสถียรภาพของระบบ</p>
    
    <h2>3. การจำกัดความรับผิดชอบ</h2>
    <p>เราพยายามอย่างเต็มที่เพื่อให้ข้อมูลมีความถูกต้องแต่ไม่มีการรับประกันความเสียหายที่อาจเกิดขึ้นจากการใช้งานในกรณีสุดวิสัย</p>
    
    <h2>4. การเปลี่ยนแปลงข้อตกลง</h2>
    <p>เราขอสงวนสิทธิ์ในการเปลี่ยนแปลงข้อตกลงนี้โดยไม่ต้องแจ้งให้ทราบล่วงหน้า โดยการเปลี่ยนแปลงจะมีผลทันทีที่ประกาศบนเว็บไซต์</p>
  `;

  const cookieContent = `
    <h1>นโยบายคุกกี้ (Cookie Policy)</h1>
    <p>เราใช้คุกกี้เพื่อเพิ่มประสิทธิภาพในการใช้งานเว็บไซต์ และมอบประสบการณ์การใช้งานที่ดีที่สุดให้แก่คุณ</p>
    
    <h2>1. คุกกี้คืออะไร?</h2>
    <p>คุกกี้คือไฟล์ข้อความขนาดเล็กที่ถูกจัดเก็บไว้ในอุปกรณ์ของคุณเมื่อคุณเข้าชมเว็บไซต์ของเรา เพื่อจดจำการตั้งค่าและกิจกรรมของคุณ</p>
    
    <h2>2. ประเภทของคุกกี้ที่เราใช้</h2>
    <ul>
      <li><strong>คุกกี้ที่จำเป็น (Essential Cookies):</strong> เพื่อให้เว็บไซต์ทำงานได้ตามปกติ</li>
      <li><strong>คุกกี้เพื่อการวิเคราะห์ (Analytical Cookies):</strong> เพื่อทำความเข้าใจพฤติกรรมการใช้งานและปรับปรุงระบบ</li>
    </ul>
    
    <h2>3. การจัดการคุกกี้</h2>
    <p>คุณสามารถตั้งค่าเบราว์เซอร์ของคุณเพื่อปฏิเสธหรือลบคุกกี้ได้ทุกเมื่อ อย่างไรก็ตาม การปิดใช้งานคุกกี้อาจส่งผลให้บางฟีเจอร์ของเว็บไซต์ทำงานได้ไม่สมบูรณ์</p>
  `;

  // Update Privacy Policy
  await prisma.page.upsert({
    where: { slug: 'privacy' },
    update: { 
      title: 'นโยบายความเป็นส่วนตัว',
      content: privacyContent,
      isPublished: true 
    },
    create: {
      title: 'นโยบายความเป็นส่วนตัว',
      slug: 'privacy',
      content: privacyContent,
      isPublished: true
    }
  });

  // Update Terms of Service
  await prisma.page.upsert({
    where: { slug: 'terms' },
    update: { 
      title: 'ข้อตกลงและเงื่อนไขการใช้งาน',
      content: termsContent,
      isPublished: true 
    },
    create: {
      title: 'ข้อตกลงและเงื่อนไขการใช้งาน',
      slug: 'terms',
      content: termsContent,
      isPublished: true
    }
  });

  // Update Cookie Policy
  await prisma.page.upsert({
    where: { slug: 'cookie-policy' },
    update: { 
      title: 'นโยบายคุกกี้',
      content: cookieContent,
      isPublished: true 
    },
    create: {
      title: 'นโยบายคุกกี้',
      slug: 'cookie-policy',
      content: cookieContent,
      isPublished: true
    }
  });

  console.log('All legal contents updated successfully!');
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
